CREATE TABLESPACE ddshell_quickstart
  NOLOGGING 
  DATAFILE '&1/ddshell_quickstart1.dbf' SIZE 100M REUSE 
  AUTOEXTEND 
  ON NEXT 100M MAXSIZE UNLIMITED EXTENT MANAGEMENT LOCAL 
  SEGMENT SPACE MANAGEMENT  AUTO 
/

CREATE USER ddshell PROFILE DEFAULT
  IDENTIFIED BY ddshell DEFAULT TABLESPACE ddshell_quickstart
  ACCOUNT UNLOCK;
GRANT CONNECT TO ddshell;
GRANT RESOURCE TO ddshell;
GRANT UNLIMITED TABLESPACE TO ddshell;
/