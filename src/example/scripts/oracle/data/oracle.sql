CREATE TABLESPACE ddshell_example
  NOLOGGING 
  DATAFILE '&1/ddshell_example1.dbf' SIZE 100M REUSE 
  AUTOEXTEND 
  ON NEXT 100M MAXSIZE UNLIMITED EXTENT MANAGEMENT LOCAL 
  SEGMENT SPACE MANAGEMENT  AUTO 
/

CREATE USER ddshell_example PROFILE DEFAULT
  IDENTIFIED BY ddshell_example DEFAULT TABLESPACE ddshell_example
  ACCOUNT UNLOCK;
GRANT CONNECT TO ddshell_example;
GRANT RESOURCE TO ddshell_example;
GRANT UNLIMITED TABLESPACE TO ddshell_example;
/